    public void TableAuteur(){
        String[] auteur = {"ID_EMPRUNTEUR","DEBUT","FIN","RENDU"};
        String [] afficher = new String [5];
        DefaultTableModel model = new DefaultTableModel(null,auteur);
        String sql = "select * from emprunt";
        try {
             Class.forName("org.sqlite.JDBC");
            con = DriverManager.getConnection("jdbc:sqlite:biblio.db" );
            Statement st = con.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()){
            afficher[0] = rs.getString("id_emprunteur");
            afficher[1] = rs.getString("debut");
            afficher[2] = rs.getString("fin");
            afficher[3] = rs.getString("rendu");
            
            model.addRow(afficher);
            }
            jTable1.setModel(model);
            con.close();
        }catch (Exception e){
            e.printStackTrace();
    }
       
    }
